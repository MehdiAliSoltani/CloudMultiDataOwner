    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jCheckBox1.isSelected() || jCheckBox2.isSelected() || jCheckBox3.isSelected()) {
            int userId = (ConstAndVars.USERS.size()) + 1;
            boolean flag = true;
            for (GroupMember gm : ConstAndVars.USERS) {
                if (gm.getUserName().equals(jTextField1.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "username has already existed");
                    flag = false;
                }
            }
            if (flag) {
                GroupMember gm = new GroupMember(userId, jTextField1.getText(), jPasswordField1.getText());
                gm.changePermission(setPermission());
                ConstAndVars.USERS.add(gm);
                JOptionPane.showMessageDialog(rootPane, "The user " + jTextField1.getText() + " has been registerd");

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "You should select one of Search, Upload, Download");
        }
        resetCheckBoxes();


    }//GEN-LAST:event_jButton1ActionPerformed

