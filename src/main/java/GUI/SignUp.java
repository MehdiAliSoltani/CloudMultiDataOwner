    private byte setPermission() {
        byte perm = 0b00000000;
        if (jCheckBox1.isSelected()) {

            perm = (byte) (perm | 0b00000001);
        }
        if (jCheckBox2.isSelected()) {
            perm = (byte) (perm | 0b00000010);
        }
        if (jCheckBox3.isSelected()) {
            perm = (byte) (perm | 0b00000100);
        }
        return perm;

    }

